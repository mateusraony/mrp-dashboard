export function getNextTier1Event() {
  const now = new Date();
  return macroEvents
    .filter(e => e.tier === 1 && new Date(e.datetime_brt) > now)
    .sort((a, b) => new Date(a.datetime_brt) - new Date(b.datetime_brt))[0];
}